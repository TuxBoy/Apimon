_composer() {
    docker exec -it -u 1000 apimon_php php composer.phar ${@:1}
}

_artisan() {
    docker exec -it -u 1000 apimon_php php artisan ${@:1}
}

case $1 in
    init)
        docker compose up -d
        _composer install
        echo "Waiting database to start"
        sleep 10
        _artisan db:seed
        _artisan l5-swagger:generate
        ;;
    start)
        docker compose up -d
        ;;
    artisan)
        _artisan "${@:2}"
        ;;
    composer)
        _composer "${@:2}"
        ;;
    *)
cat <<EOS
Usage: $0 artisan           Run laravel artisan command
  or : $0 composer          Run composer
EOS
;;
esac
